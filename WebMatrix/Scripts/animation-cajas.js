$(document).ready(function() {
    $(".menu-element1").hover(function () {
        var altura = $(".menu-element1 .linea1").height();
        var altura2 = $(".menu-element1 .linea2").height();
		    $(".menu-element1 .icon-menu img").stop().animate({ height: "130%", width: "130%"}, 300);
			$(".menu-element1 .linea1").stop().animate({ marginTop: 127 - (altura + altura2) }, 400);
		},function(){
			$(".menu-element1 .icon-menu img").stop().animate({ height: "100%", width: "100%"}, 300);
		    $(".menu-element1 .linea1").stop().animate({ marginTop: "180px" }, 300);
		});
});

